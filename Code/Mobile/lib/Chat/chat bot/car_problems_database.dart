  // Getting a random sample of problems for use as examples for the model
  static List<Map<String, String>> getSampleProblems(int count) {
    if (count >= carProblems.length) return carProblems;
    
    // Creating a copy of the list
    List<Map<String, String>> shuffled = List.from(carProblems);
    
    // Shuffling the examples
    shuffled.shuffle();
    
    // Returning the required number
    return shuffled.take(count).toList();
  }
